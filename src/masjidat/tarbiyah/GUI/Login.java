    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        LoginOperation loginOperation = new LoginOperation();

        if (username.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Username Kosong!!");
        } else if (password.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Password Kosong!!");
        } else {
            try {
                loginOperation.chekUser(username.getText(), password.getText(), this);
            } catch (SQLException e) {
                throw new RuntimeException(e);
            } catch (ClassNotFoundException e) {
                throw new RuntimeException(e);
            }
        }

    }//GEN-LAST:event_jButton1MouseClicked

